public class LineCoordinate {
    public int startX;
    public int startY;
    public int endX;
    public int endY;

    public LineCoordinate(int startX, int startY, int endX, int endY)
    {
        this.startX = startX;
        this.startY = startY;
        this.endX = endX;
        this.endY = endY;
    }
}
